package com.xworkz.interview.pattern;

public class FiveSixStar {
    public static void main(String[] args) {
        int i;
        int j;
        for(i=1;i<=5;i++){
            for(j=1;j<=6;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
