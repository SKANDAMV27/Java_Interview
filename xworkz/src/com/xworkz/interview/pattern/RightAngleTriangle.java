package com.xworkz.interview.pattern;

public class RightAngleTriangle {
    public static void main(String[] args) {
        int i;
        int j;
//        int n;
        for(i=1;i<=5;i++){
            for(j=1;j<=i;j++){
                System.out.print(" * ");
            }
            System.out.println();
        }
    }

}
